<?php


/*
-------"indexed array"
Most every programming language index starts with 0 and not 1
which is called zero indexed
The first value is index 0

  index0   index 1
("value1", "value2")

example:
*/

$row1 = array("value1", "value2");


/*
--------"associative array" or "key value pair"

This is a key value pair  "date" => "date1"
The arrow function "=>" assigns the value to the key
Each pair is separated by a comma ("date" => "date1", "shift" => "open")

*/

$row1 = array("key1" => "value1", "key2" => "value2");